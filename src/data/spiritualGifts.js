// Calculate spiritual gift scores from 72 survey responses
export function calculateGiftScores(answers) {
  const scores = [];
  // Each gift has 3 questions (24 gifts Ã— 3 = 72 questions)
  for (let i = 0; i < 24; i++) {
    const score = (answers[i] || 0) + (answers[i + 24] || 0) + (answers[i + 48] || 0);
    scores.push(score);
  }
  return scores;
}

// Get top 3 gifts with their indices
export function getTop3Gifts(scores) {
  const indexed = scores.map((score, index) => ({ score, index }));
  indexed.sort((a, b) => {
    if (a.score !== b.score) return b.score - a.score;
    return a.index - b.index;
  });
  return indexed.slice(0, 3);
}
